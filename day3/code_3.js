arr = ["apple", "banana", "cherry", "mango", "orange"];

arr.push("papaya"); // Adding an element to the end
console.log(arr);

arr.pop(); // Removing the last element
console.log(arr);

arr.unshift("kiwi"); // Adding an element to the beginning
console.log(arr);

arr.shift(); // Removing the first element
console.log(arr);

console.log(arr.includes("mango"));

console.log(arr.indexOf("banana"));